#include "minishell.h"

int	numbered_args(char **s)
{
	int	i;
	int	j;

	i = 0;
	while (s[i])
	{
		j = 0;
		while (s[i][j])
		{
			if (!(s[i][j] >= '0' && s[i][j] <= '9'))
				return (1);
			j++;
		}
		i++;
	}
	return (0);
}
